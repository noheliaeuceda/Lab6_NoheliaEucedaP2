    private void btn_imprimirFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imprimirFacturaActionPerformed
        // TODO add your handling code here:
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_producto.getModel();
         DefaultListModel modeloL = (DefaultListModel) jl_productos.getModel();
         double total=0, subtotal=0;
        String nombre, email, rtn, cai;
        nombre = tf_nombreF.getText();
        email = tf_email.getText();
        rtn = tf_rtn.getText();
        cai = tf_cai.getText();
        ArrayList<Producto> lista = new ArrayList();
        
        for (int i = 0; i < modeloL.getSize(); i++) {
            lista.add(((Producto)modeloL.getElementAt(i)));
            subtotal +=((Producto)modeloL.getElementAt(i)).getPrecioCosto();
            total +=((Producto)modeloL.getElementAt(i)).getPrecioVenta();
        }
        Ventas v= new Ventas(total, new Date());
        i.Agregar(v);
        Factura fact = new Factura(nombre,email, rtn, cai, new Date());
        fact.setListaP(lista);
        fact.imprimeFactura(this, subtotal, total);
        tf_nombreF.setText("");
        tf_email.setText("");
        tf_rtn.setText("");
        tf_cai.setText("");
        modeloL.clear();
        modelo.removeAllElements();
        
    }//GEN-LAST:event_btn_imprimirFacturaActionPerformed

