    private void btn_modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_modificarMouseClicked
        // TODO add your handling code here:
        try {
            inventario.escribirArchivo();
            int i = (Integer) js_indice.getValue();
            String nombre;
            double c, v;
            if (i >= 0) {
                nombre = JOptionPane.showInputDialog("Ingrese nuevo nombre");
                c = Integer.parseInt(JOptionPane.showInputDialog("Ingrese nuevo Precio de costo"));
                v = Integer.parseInt(JOptionPane.showInputDialog("Ingrese nuevo Precio de venta"));
                inventario.lista.get(i).setNombre(nombre);
                inventario.lista.get(i).setPrecioCosto(c);
                inventario.lista.get(i).setPrecioVenta(v);

                inventario.cargarArchivo();
                String salida = inventario.cargarArchivo();
                ta_inventario.setText(salida);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jd_inventario, "No se pudo modificar");
        }

    }//GEN-LAST:event_btn_modificarMouseClicked

