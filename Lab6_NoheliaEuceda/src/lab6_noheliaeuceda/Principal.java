    private void btn_guardarBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_guardarBMouseClicked
        // TODO add your handling code here:
        try {

            String nombre, estado, tipo;
            double tamanio, precioCosto, precioVenta;

            nombre = tf_nombreB.getText();
            precioCosto = (double) js_costo.getValue();
            precioVenta = (double) js_precioB.getValue();
            estado = (String) cb_estado.getSelectedItem();
            tamanio = (double) js_tamanio.getValue();
            tipo = (String) cb_tipo.getSelectedItem();
            Bebida b = new Bebida(estado, tamanio, tipo, precioCosto, precioVenta, nombre);
            listaP.add(b);
            inventario.agregar(b);

            tf_nombreB.setText("");
            js_costo.setValue(0);
            js_precioB.setValue(0);
            cb_estado.setSelectedIndex(0);
            js_tamanio.setValue(0);
            cb_tipo.setSelectedIndex(0);
            JOptionPane.showMessageDialog(jd_nuevaBebida, "Agregado con exito");
            jd_nuevaBebida.setVisible(false);

        } catch (Exception e) {
            System.out.println(e.getMessage());
            e.printStackTrace();

            JOptionPane.showMessageDialog(jd_nuevaBebida, "No se pudo agregar");

        }
    }//GEN-LAST:event_btn_guardarBMouseClicked

